    public void agregarListeners(){
        for (int i = 0; i < botones.length; i++) {
            for (int j = 0; j < botones.length; j++) {
                botones[i][j].addFocusListener(new FocusAdapter(botones[i][j]));
                botones[i][j].addKeyListener(new KeyAdapter(botones[i][j]));
            }
        }
        
    }
    
    public class FocusAdapter extends java.awt.event.FocusAdapter{
        
        JButton boton;

        public FocusAdapter(JButton boton) {
            this.boton = boton;
        }

       @Override
       public void focusLost(java.awt.event.FocusEvent evt) {
            cargarValor(boton);
       }
   }
    
    public class KeyAdapter extends java.awt.event.KeyAdapter{
        JButton boton;

        public KeyAdapter(JButton boton) {
            this.boton = boton;
        }
        @Override
         public void keyReleased(java.awt.event.KeyEvent evt) {
            teclaOprimida(boton,evt);
        }
    }
    
